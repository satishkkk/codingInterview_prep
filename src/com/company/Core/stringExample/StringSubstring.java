package com.company.Core.stringExample;

public class StringSubstring {

    public static void main(String[] args) {
        String s = "ABCDEFGHIJK";

        System.out.println(s.substring(0,5));// print string index from 0 to 4
        System.out.println(s.substring(7));//print string index from 7 to end

    }
}
